#include "ft_printf.h"

int	ft_phex(unsigned int p)
{
	static int	i;

	if (p > 16)
	{
		i++;
		ft_phex(p / 16);
	}
	if (p < 16)
		i++;
	p = p % 16;
	if (p > 9)
		p = p + 55;
	else
		p = p + 48;
	write(1, &p, 1);
	return (i);
}
